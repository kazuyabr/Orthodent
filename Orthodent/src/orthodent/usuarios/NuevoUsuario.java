    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        
        int idRol = 0;
        
        if(this.admin.isSelected()){
            idRol = 1;
        }
        else if(this.asistente.isSelected()){
            idRol = 2;
        }
        else{
            idRol = 3;
        }
        
        String nombre = this.nombres.getText();
        String apellidoPat = this.apellidoPat.getText();
        String apellidoMat = this.apellidoMat.getText();
        String email = this.email.getText();
        
        String nombreUsuario = null;
        
        if(email.contains("@")){
            nombreUsuario = email.substring(0, email.indexOf("@"));
        }
        
        String contraseña1 = this.contrasena1.getText();
        String contraseña2 = this.contrasena2.getText();
        
        String telefono = this.telefono.getText();
        
        boolean aux = validarCamposObligatorios(nombre,apellidoPat,email,contraseña1,contraseña2);
        
        if(aux){
            if(contraseña1.equals(contraseña2)){
                try {
                    boolean respuesta = Autenticacion.crearUsuario(idRol, nombre, apellidoPat, apellidoMat, 
                                                            nombreUsuario, contraseña1, email, telefono);
                    
                    if(respuesta){
                        System.out.println("Agregado :)");
                    }
                    else{
                        System.out.println("Algo ocurrio mal =/");
                    }
                } catch (Exception ex) {
                    System.out.println("");
                }
                
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this,
                    "Las contraseñas no coinciden!",
                    "Orthodent",
                    JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            System.out.println("Marcar campos obligatorios faltantes!!");
        }
    }//GEN-LAST:event_aceptarActionPerformed

    private boolean validarCamposObligatorios(String nombre, String apellidoPat, String email, String contraseña1, String contraseña2){
        
        if(nombre.equals("")){
            return false;
        }
        
        if(apellidoPat.equals("")){
            return false;
        }
        
        if(email.equals("")){
            return false;
        }
        
        if(contraseña1.equals("")){
            return false;
        }
        
        if(contraseña2.equals("")){
            return false;
        }
        
        return true;
    }
    
